package br.com.meganet.servicos.vo;

public class Total {

	private String numero_cobrancas;
	private String numero_pago_bloqueado;
	private String numero_pago_liberado;
	private String valor_cobrado;
	private String valor_pago_bloqueado;
	private String valor_pago_liberado;
	
	
	public String getNumero_cobrancas() {
		return numero_cobrancas;
	}
	public void setNumero_cobrancas(String numeroCobrancas) {
		numero_cobrancas = numeroCobrancas;
	}
	public String getNumero_pago_bloqueado() {
		return numero_pago_bloqueado;
	}
	public void setNumero_pago_bloqueado(String numeroPagoBloqueado) {
		numero_pago_bloqueado = numeroPagoBloqueado;
	}
	public String getNumero_pago_liberado() {
		return numero_pago_liberado;
	}
	public void setNumero_pago_liberado(String numeroPagoLiberado) {
		numero_pago_liberado = numeroPagoLiberado;
	}
	public String getValor_cobrado() {
		return valor_cobrado;
	}
	public void setValor_cobrado(String valorCobrado) {
		valor_cobrado = valorCobrado;
	}
	public String getValor_pago_bloqueado() {
		return valor_pago_bloqueado;
	}
	public void setValor_pago_bloqueado(String valorPagoBloqueado) {
		valor_pago_bloqueado = valorPagoBloqueado;
	}
	public String getValor_pago_liberado() {
		return valor_pago_liberado;
	}
	public void setValor_pago_liberado(String valorPagoLiberado) {
		valor_pago_liberado = valorPagoLiberado;
	}
	
}
